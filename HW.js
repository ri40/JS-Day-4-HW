const arr = [
    {name:"Lana", height:165, gender:"female", mass:60, eyeColor:"brown"},
    {name:"Mias", height:160, gender:"female", mass:56, eyeColor:"brown"},
    {name:"Ahmad", height:179, gender:"male", mass:80, eyeColor:"black"},
    {name:"Ali", height:180, gender:"male", mass:90, eyeColor:"blue"}
];
/*=========================================================*/
console.log("\n========================================MAP========================================");
/*=========================================================*/

console.log("\n--------------------1-MAP-name&height--------------------------");
const arrMap = arr.map(arr=>({ name: arr.name , height: arr.height}));
console.log(arrMap);

/*=========================================================*/

console.log("\n--------------------2-MAP-firstNames--------------------------");
const arrMap1 = arr.map(arr=>arr.name );
console.log(arrMap1);

/*=========================================================*/
console.log("\n========================================REDUCE========================================");
/*=========================================================*/

console.log("\n--------------------1-REDUCE-total-mass-------------------------");
let arrRed = arr.reduce((p,c)=>p+c.mass,0);
console.log(arrRed);

/*=========================================================*/

console.log("\n--------------------2-REDUCE-total-number-------------------------");
let arrRed1 = arr.reduce((p,c)=>p+c.name.length,0);
console.log(arrRed1);

/*=========================================================*/
console.log("\n========================================FILTER========================================");
/*=========================================================*/

console.log("\n--------------------1-FILTER-mass->100-----------------------");
let arrFit = arr.filter((c)=>c.mass>100);
console.log(arrFit);

/*=========================================================*/

console.log("\n--------------------2-FILTER-female-------------------------");
let arrFit1 = arr.filter((c)=> c.gender === "female");
console.log(arrFit1);

/*=========================================================*/
console.log("\n========================================SORT========================================");
/*=========================================================*/

console.log("\n--------------------1-SORT-name-------------------------");
let arrSor = arr.sort((a,b)=>b.name-a.name)
console.log(arrSor);

/*=========================================================*/

console.log("\n--------------------2-SORT-gender-------------------------");
let arrSor1 = arr.sort((a,b)=> b.gender-a.gender)
console.log(arrSor1);

/*=========================================================*/
console.log("\n========================================EVERY========================================");
/*=========================================================*/

console.log("\n--------------------1-EVERY-blue-eyes-------------------------");
let arrEve = arr.every(arr => arr.eyeColor ==="blue");
console.log(arrEve);

/*=========================================================*/

console.log("\n--------------------2-EVERY-male-------------------------");
let arrEve1 = arr.every(arr => arr.gender ==="male");
console.log(arrEve1);

/*=========================================================*/
console.log("\n========================================SOME========================================");
/*=========================================================*/

console.log("\n--------------------1-SOME-male-------------------------");
let arrSom = arr.some(arr => arr.gender ==="male");
console.log(arrSom);

/*=========================================================*/

console.log("\n--------------------2-SOME-mass-<50------------------------");
let arrSom1 = arr.some(arr => arr.mass<50);
console.log(arrSom1);